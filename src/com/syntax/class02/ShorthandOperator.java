package com.syntax.class02;

public class ShorthandOperator {

	public static void main(String[] args) {
		// short hand operator is a shorter way to perform arithmetic operators
		// called short hand operator or short hand assignment opperator
		
		int num=20;
		num=num+100;
		System.out.println(num);
		
		num=num-60;
		System.out.println(num);
		
		int number=100;
		number+=100;//number=number+100
		
		System.out.println(number);//200
		
		number-=30;//number=number-30
		System.out.println(number);//170
		
		number/=10;
		System.out.println(number);//17
		
		number*=2;
		System.out.println(number);

	}

}
