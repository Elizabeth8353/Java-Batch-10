package com.syntax.notes;

public class DefaultValues {

	public static void main(String[] args) {
		/*
		 * compiler can initialize variables to their default values :
		 * default value int-> 0
		 * default of double ->0.0
		 * default of boolean -> false
		 * default of String and all non primitive is null
		 * default for char is -> invisible characters 
		 * 
		 */
		
		
String str = null;
System.out.println(str);

byte b = 0;
System.out.println(b);

short s = 0;
System.out.println(s);

int i = 0;
System.out.println(i);

long l = 0l;
System.out.println(l);
 double d = 0.0;
 System.out.println(d);
 char c = 0;
 System.out.println(c);//invisible 
 
	}

}
